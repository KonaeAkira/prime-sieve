public:
	number(uint64_t prime): block(prime / 210), rest(key[prime % 210]) {}
	
	number(uint64_t prime, uint64_t begin)
	{
		uint64_t multiplier = std::max(prime, begin / prime + (begin % prime != 0));
		while (value[key[multiplier % 210]] != multiplier % 210)
			++multiplier;
		block = prime * multiplier / 210;
		rest = key[prime * multiplier % 210];
	}
	
	uint64_t get_block() const { return block; }
	uint16_t get_rest() const { return rest; }
	uint64_t get_number() const { return block * 210 + value[rest]; }
	
	number &operator += (const number rhs)
	{
		block += times[rest][rhs.rest] * rhs.block + adjust[rest][rhs.rest];
		rest = target[rest][rhs.rest];
		return *this;
	}
